def _ensure_tags():
    """Создаёт теги, если их ещё нет. Возвращает список InfoTag."""
    tags = []
    for order, (code, label) in enumerate(STANDALONE_TAGS, start=1):
        tag, _ = InfoTag.objects.get_or_create(
            code=code,
            defaults={"label": label, "order": order},
        )
        tags.append(tag)
    return tags


def _random_content(sections_source, min_sections=4, max_sections=7):
    """Собирает крупный markdown-контент из случайных секций (заголовки, списки, выделения)."""
    count = random.randint(min_sections, min(max_sections, len(sections_source)))
    chosen = random.sample(sections_source, count)
    return "\n\n".join(chosen)


def _random_article_data(titles_source, paragraphs_source, sections_source):
    """Возвращает dict с title, description, content (большой markdown)."""
    title = random.choice(titles_source)
    description = random.choice(paragraphs_source)[:500]
    content = _random_content(sections_source)
    return {"title": title, "description": description, "content": content}


class Command(BaseCommand):
    help = (
        "Создаёт теги (InfoTag) и статьи без породы (Article, breed=None) "
        "со случайным текстом о собаках. Идемпотентно по slug."
    )

    def add_arguments(self, parser):
        parser.add_argument(
            "--count",
            type=int,
            default=10,
            help="Сколько статей создать (по умолчанию 10).",
        )

    def handle(self, *args, **options):
        count = options["count"]
        if count < 1:
            self.stdout.write(self.style.WARNING("count должен быть >= 1, установлено 1."))
            count = 1

        tags = _ensure_tags()
        self.stdout.write(self.style.SUCCESS(f"Теги: {len(tags)} шт."))

        all_photos = get_all_photo_paths()
        if all_photos:
            self.stdout.write(
                self.style.SUCCESS(f"Найдено фото в test-photos: {len(all_photos)} шт.")
            )
        else:
            self.stdout.write(
                self.style.WARNING(
                    "В test-photos нет картинок — image_preview у статей не заполняется."
                )
            )

        created = 0
        updated = 0

        for i in range(1, count + 1):
            slug = f"sobaki-{i}"
            data = _random_article_data(DOG_TITLES, DOG_PARAGRAPHS, DOG_MARKDOWN_SECTIONS)

            article, was_created = Article.objects.get_or_create(
                slug=slug,
                defaults={
                    "title": data["title"],
                    "description": data["description"],
                    "content": data["content"],
                    "status": InfoStatus.PUBLISHED,
                    "breed": None,
                },
            )
            if was_created:
                created += 1
            else:
                updated += 1
                article.title = data["title"]
                article.description = data["description"]
                article.content = data["content"]
                article.status = InfoStatus.PUBLISHED
                article.breed = None
                article.save()

            # Картинка из test-photos: по одному файлу на статью (циклически)
            if all_photos:
                photo_path = all_photos[(i - 1) % len(all_photos)]
                save_name = f"info_models/standalone_{slug}{photo_path.suffix}"
                if assign_photo_from_path(
                    article, "image_preview", photo_path, save_name
                ):
                    article.save()

            # Назначаем случайное подмножество тегов (от 1 до всех)
            k = random.randint(1, len(tags))
            chosen_tags = random.sample(tags, k)
            article.tags.set(chosen_tags)

        self.stdout.write(
            self.style.SUCCESS(
                f"Готово. Статей создано: {created}, обновлено: {updated}."
            )
        )
